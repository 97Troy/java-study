public class ForTest {
    public static void main(String[] args) {
        for(int i = 1;i <= 5; i++){
            System.out.println("HelloWorld!");
        }
        //打印1-5
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }
        //打印5-1
        for(int i = 5; i >= 1; i--){
            System.out.println(i);
        }
    }


}
